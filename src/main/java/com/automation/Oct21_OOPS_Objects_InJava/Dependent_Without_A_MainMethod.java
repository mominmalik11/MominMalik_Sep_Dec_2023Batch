package com.automation.Oct21_OOPS_Objects_InJava;

public class Dependent_Without_A_MainMethod {
	
	public static void red() {
		System.out.println("This is static red");
	}
	public static void blue() {
		System.out.println("This is static blue");
	}
	public static void green() {
		System.out.println("This is static green");
	}
	public static void yellow() {
		System.out.println("This is static yellow");
	}

	public void violet() {
		System.out.println("This is nonstatic violet");
	}
	
	public void pink() {
		System.out.println("This is nonstatic pink");
	}

}
